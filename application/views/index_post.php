              <p>
                抹茶紅豆發糕
              </p>
              <p>
                <a class="post_img">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/1411549443159.jpg"
                  alt="1411549443159.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                <a href="#">
                  發糕粉
                </a>
                300公克，
                <a href="#">
                  抹茶粉
                </a>
                9公克，水180公克，細砂糖165公克，
                <a href="#">
                  蜜紅豆
                </a>
                60公克
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1.先將發糕粉和抹茶粉一起過篩。
                <br />
                2.將作法1及水、細砂糖放入鋼盆中，以打蛋器攪拌至無糖顆粒狀的面煳，靜置約20分鐘。
                <br />
                3.蒸鍋預熱至水開，將作法2的面煳裝入模型中，撒上蜜紅豆，放入蒸籠以中火蒸約20分左右即可取出。
              </p>
              <p>
                <br />
              </p>
              <p>
                <br />
              </p>
              <p>
                黑糖發糕
              </p>
              <p>
                <a class="post_img">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115494868511.jpg"
                  alt="14115494868511.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                <a href="#">
                  發糕粉
                </a>
                300公克，水180公克，
                <a href="#">
                  黑糖
                </a>
                180公克
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1.先將發糕粉過篩；黑糖和水攪拌均勻至無糖顆粒狀備用。
                <br />
                2.將作法1的所有材料放入鋼盆中，以打蛋器攪拌至均勻無糖顆粒狀的面煳，靜置約20分鐘，備用。
                <br />
                3.蒸鍋預熱至水開后，將作法2的面煳裝入模型中，放入蒸籠以中火蒸約20分鐘左右即可取出。
              </p>
              <p>
                <br />
              </p>
              <p>
                <br />
              </p>
              <p>
                紫薯發糕
              </p>
              <p>
                <a href="#" target="_blank">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115495127794.jpg"
                  alt="14115495127794.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                <a href="#">
                  紫薯
                </a>
                250克，
                <a href="#">
                  麵粉
                </a>
                300克，白糖30克，
                <a href="#">
                  酵母粉
                </a>
                5克，水約250-280克，
                <a href="#">
                  葡萄乾
                </a>
                少許，
                <a href="#">
                  蜜棗
                </a>
                少許
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1.紫薯蒸熟去皮後放入盆中壓成泥
                <br />
                2.紫薯泥中加入清水和白糖，用攪拌機打成細膩的紫薯糊，加入發酵粉攪拌均勻
                <br />
                3.加入麵粉
                <br />
                4.用刮刀和筷子將麵粉與紫薯糊拌勻成麵糰狀
                <br />
                5.將麵糰全部盛入8寸蛋糕模具中，用刮刀稍微粘點水，將麵糰表面抹平整光滑
                <br />
                6.發酵至2倍大時，在表面撒上些葡萄乾和蜜棗粒，冷水上鍋，大火燒開後轉中火蒸25分鐘，關火后燜5分鐘即可，出鍋放涼后切塊裝盤
              </p>
              <p>
                <br />
              </p>
