              <p>
                紅糖發糕
              </p>
              <p>
                <a class="post_img">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115496358276.jpg"
                  alt="14115496358276.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                A:
                <br />
                <a href="#">
                  低筋麵粉
                </a>
                或
                <a href="#">
                  自發麵粉
                </a>
                -200g
                <br />
                <a href="#">
                  粘米粉
                </a>
                -50g
                <br />
                <a href="#">
                  小蘇打
                </a>
                -1/4茶匙
                <br />
                <a href="#">
                  發粉
                </a>
                /
                <a href="#">
                  泡打粉
                </a>
                -3茶匙（tspn）×如果用自發麵粉，發粉就只需要1茶匙
                <br />
                <br />
                B:
                <br />
                水- 200g
                <br />
                <a href="#">
                  紅糖
                </a>
                或
                <a href="#">
                  黑糖
                </a>
                - 120～150g
                <br />
                鹽- 1/4茶匙
                <br />
                <a href="#">
                  香蘭葉
                </a>
                -2片
                <br />
                <br />
                C:
                <br />
                <a href="#">
                  雞蛋
                </a>
                - 1顆
                <br />
                <a href="#">
                  玉米油
                </a>
                - 3湯匙
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1. 先把乾粉類及發粉混合後過篩一遍備用。
                <br />
                <br />
                2. 把水， 紅糖， 香蘭葉及鹽在一小鍋內用慢火煮至紅糖充分溶化，過濾后攤涼。
                <br />
                <br />
                3. 紅糖液攤涼后，在開始製作發糕的同時， 在蒸鍋內燒1500cc的水， 以備蒸糕用。
                <br />
                <br />
                4. 把雞蛋液攪打均勻後過濾，加入攤涼的紅糖水， 用力攪打約2~3分鐘至起豐富的泡沫。
                <br />
                <br />
                5. 加入3大匙的玉米油或食油拌均。
                <br />
                <br />
                6. 把過篩好的乾粉類加入， 慢慢攪拌均勻。
                <br />
                <br />
                7. 把12個蛋糕紙杯放在蛋塔杯內，再倒入混合物至9分滿，再排在蒸籠內，用大火蒸15 ～ 20分鐘即可。(切記：要等水大沸騰后才可下鍋蒸，
                這樣發糕才會大發）
                <br />
                <br />
                <br />
                小貼士：
                <br />
                1. 如果要做素的， 就不用加雞蛋， 水要增加至250g或是以50g的椰漿取代雞蛋。
                <br />
                <br />
                2. 如果沒有低筋麵粉， 就用170g的普通麵粉+30g的玉米澱粉即可。 （15%的玉米澱粉）。
                <br />
                <br />
                3。蒸出來的發糕如果有鄒皮或結硬皮的現象，應該是鍋蓋滴水，或是鍋蓋太密導致鍋內的壓力過高，可以再蒸糕時插支竹籤或環保筷在鍋蓋下，讓鍋蓋下有個小小縫可以透些氣。
                <br />
                <br />
                4。把鍋蓋內的水蒸氣抹乾才蓋上蒸糕，減少滴水的情形發生。
                <br />
                <br />
                5。做素的可以添加一至兩匙的大豆粉，我加Amway的All Plant Protein Powder-15g。
              </p>
