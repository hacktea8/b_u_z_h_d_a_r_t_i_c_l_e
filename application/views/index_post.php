              <h2>
                做法
              </h2>
              <p>
                1、黑糖2匙加水3匙隔水加熱至黑糖完全融化。
                <br />
                2、酵母半小匙加溫水2匙用湯匙拌勻。
                <br />
                3、大碗中加入薏仁粉5匙，低筋麵粉5匙和泡打粉半小匙，用湯匙拌勻；加入步驟（1）和步驟（2）用湯匙拌勻；再加入適量掰碎的蜂蜜桃仁用湯匙拌勻。
                <br />
                4、將步驟（3）倒入模具，震動幾下模具將氣泡震出；置溫暖處發酵30分鐘至兩倍大。
                <br />
                5、蒸鍋加熱至出蒸氣將步驟（4）放入，蒸15分鐘。
                <br />
                6、蒸好後等5分鐘再開蓋。
                <br />
              </p>
