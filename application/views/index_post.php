              <p>
                1、我是用電蒸鍋融化黑糖，關閉電源後將蓋子蓋上作為發酵的環境。
                <br />
                2、蒸好後5分鐘再開蓋是為避免發糕回縮。
                <br />
                3、發糕頂部要開花蒸時要待蒸鍋出蒸氣再放入。
                <br />
                4、化酵母要用溫水，靜置一下再拌就很容易化開。
