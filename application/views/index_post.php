                <br />
              </p>
              <p>
                玉米面發糕
              </p>
              <p>
                <a href="#" target="_blank">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115495441330.jpg"
                  alt="14115495441330.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                普通
                <a href="#">
                  麵粉
                </a>
                250g，
                <a href="#">
                  玉米面
                </a>
                50g，
                <a href="#">
                  牛奶
                </a>
                180g，細砂糖20g，
                <a href="#">
                  酵母
                </a>
                3g，
                <a href="#">
                  葡萄乾
                </a>
                適量
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1.牛奶微溫，加入酵母中，靜置3分鐘，攪拌均勻
                <br />
                2.將酵母溶液加入玉米面中，混合均勻
                <br />
                3.麵粉中加入細砂糖
                <br />
                4.將玉米粉溶液倒入麵粉中
                <br />
                5.將麵粉揉成團，微微收圓
                <br />
                6.方形模具放入蒸籠，墊一層濕潤紗布，將麵糰放入，放置溫暖處發酵
                <br />
                7.麵糰發酵至兩倍大時，表面撒上泡軟的葡萄乾
                <br />
                8.鍋中燒水，水開後放入蒸籠，大火蒸30分鐘左右
              </p>
              <p>
                <br />
              </p>
