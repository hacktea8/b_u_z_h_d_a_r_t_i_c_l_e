              <p>
                黑米桂花發糕
              </p>
              <p>
                <a class="post_img">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115495808554.jpg"
                  alt="14115495808554.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <p>
                黑米有很高的營養價值，此處不贅述。添加少量的粘米粉/北方稱為大米粉，會使發糕除了鬆軟以外，還有饅頭的韌性和嚼勁，表皮富有光澤，似黑珍珠，有濃濃的桂花香氣，自家私房點心，吃的安全營養又放心
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                <a href="#">
                  白面
                </a>
                <a href="#">
                  粉
                </a>
                300克
                <a href="#">
                  黑米
                </a>
                粉200克
                <a href="#">
                  粘米粉
                </a>
                /
                <a href="#">
                  大米粉
                </a>
                50克
                <a href="#">
                  發酵粉
                </a>
                5-8克
                <a href="#">
                  泡打粉
                </a>
                10-16克
                <a href="#">
                  桂花醬
                </a>
                20克
                <br />
                如果沒有桂花醬，可以製作，超市有
                <a href="#">
                  桂花
                </a>
                賣，把桂花用鹽拌勻，然後放到玻璃罐里，一層腌桂花一層
                <a href="#">
                  蜂蜜
                </a>
                ，封蓋放入冰箱，就可以了
                <br />
                如果經常做
                <a href="#">
                  豆腐花
                </a>
                ，家裡有
                <a href="#">
                  葡萄糖
                </a>
                <a href="#">
                  酸
                </a>
                <a href="#">
                  內酯
                </a>
                ，可以替代泡打粉使用葡萄糖酸內酯2：
                <a href="#">
                  小蘇打
                </a>
                1的比例佔有
                <a href="#">
                  麵粉
                </a>
                的2.5%的用量使用
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1把白麵粉和黑米麵粉及粘米粉,泡打粉混合，加入粘米粉的目的是更有嚼勁，不會過於鬆軟
                <br />
                2用溫水把發酵粉，白糖少量（是麵糰容易發酵，不是甜味劑），化開，大約300到500毫升的溫水，也就是一瓶礦泉水的量要多一些的溫水，來化開。做發糕的和面水，比饅頭和餃子的水要多，因為發糕的麵糰是稀面，也就是說，是粘稠的，沾手的，不能揉搓的，只能攪拌的。然後把桂花醬適量攪入麵糊里，口味根據各人，喜歡甜就多放。
                <br />
                3活好面，蓋上蓋子，或者濕的蒸籠布蓋上發麵盆，等面發到2-2.5倍大，也就是面之間有好多孔洞就可以了
                <br />
                4把麵糊倒入蒸鍋，在籠屜上鋪上一次性籠屜布/超市有賣，如果沒有就去藥房買個口罩，拆開做籠屜布，口罩的紗布一般是脫脂紗布，很好用，大約3元錢2個口罩。
                <br />
                5 中火蒸25-30分鐘，根據麵糊的量來決定，麵糊多，就多蒸一些時間
                <br />
                6 蒸熟了發糕，用刀切成塊，擺盤。吃不完的晾涼，裝入塑料袋，，放入冷凍室，吃的時候加熱
                <br />
              </p>
              <h2>
                小訣竅
              </h2>
              <p>
                和面要加入粘米粉少量，蒸的時候要把表面抹平，沒有烘焙用膠鏟，就用手蘸涼水把表面抹平
              </p>
