              <p>
                蜂蜜雞蛋玉米面發糕
              </p>
              <p>
                <a class="post_img">
                  <img src="http://s1.buzzhand.net/uploads/e3/3/122494/14115496801602.jpg"
                  alt="14115496801602.jpg" />
                </a>
              </p>
              <p>
                <br />
              </p>
              <h2>
                材料
              </h2>
              <p>
                <a href="#">
                  小麥麵粉
                </a>
                150克，
                <a href="#">
                  玉米面
                </a>
                50克，
                <a href="#">
                  雞蛋
                </a>
                1個，
                <a href="#">
                  牛奶
                </a>
                200克，
                <a href="#">
                  酵母粉
                </a>
                1/4茶匙，食用油20克，
                <a href="#">
                  蜂蜜
                </a>
                2湯匙，
                <a href="#">
                  紅棗
                </a>
                2個，
                <a href="#">
                  葡萄乾
                </a>
                1小把
                <br />
              </p>
              <h2>
                做法
              </h2>
              <p>
                1.【牛奶酵母水調製過程】：牛奶200克倒入乾淨的碗中
                <br />
                2.小碗放入微波爐中，高火加熱20秒左右至牛奶溫熱
                <br />
                3.取出的牛奶里加入1/4茶匙酵母
                <br />
                4.用筷子攪拌均勻成牛奶酵母水，靜置10分鐘
                <br />
                <br />
                5.【蜂蜜雞蛋乳液調製過程】：雞蛋1個磕入碗中
                <br />
                6.擠入約2湯匙蜂蜜
                <br />
                7.加入20克食用油
                <br />
                8.用筷子把蜂蜜、食用油和雞蛋液攪拌均勻，成乳化狀態
                <br />
                <br />
                9.【發糕麵糊調製過程】：小麥麵粉150克和玉米粉50克放入和面盆里
                <br />
                10.用筷子把兩種麵粉攪拌均勻
                <br />
                11.分次往麵粉里倒入蜂蜜雞蛋乳液，用筷子攪拌成帶有大量乾麵粉的乳化妝面絮
                <br />
                12.分次往乾麵粉上倒入適量的牛奶酵母水
                <br />
                13.用筷子把酵母水與乾麵粉攪拌均勻，成沒有乾麵粉的濕性團
                <br />
                14.再分次往濕性麵糰上加入適量的牛奶酵母水
                <br />
                15.用筷子把酵母水與濕性麵糰攪拌均勻，使濕性麵糰稀釋成可以流動的糊狀
                <br />
                16.用筷子挑起麵糊，可以像水滴一樣流下即可
                <br />
                <br />
                17.【發酵麵糊發酵與微波蒸制過程】：取一個可以放入微波爐里的大碗，用刷子在碗底和碗壁均勻的刷一層食用油
                <br />
                18.紅棗和葡萄乾洗凈，撕成大小合適的塊分散鋪在碗底
                <br />
                19.調製好的麵糊倒在碗里
                <br />
                20.上面蓋一層保鮮膜放在溫暖處發酵40分鐘
                <br />
                21.使麵糊發酵至碗容量的9成滿
                <br />
                22.發酵好的麵糊上再分散鋪一些紅棗和葡萄乾
                <br />
                23.碗上面倒口一個盤子，放在微波爐里，高火加熱5分鐘至麵糊熟
                <br />
